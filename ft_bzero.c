#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*z;

	z = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		*z = 0;
		z++;
		n--;
	}
}
